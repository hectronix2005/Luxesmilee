// Function to get video by context
function getVideoByContext(context, index = 0) {
    switch(context) {
        case 'service':
            const serviceKeys = Object.keys(realVideos.services);
            return realVideos.services[serviceKeys[index]] || realVideos.services.veneers;
        case 'testimonial':
            return realVideos.testimonials[index] || realVideos.testimonials[0];
        case 'doctor':
            return realVideos.doctors[`doctor${index + 1}`] || realVideos.doctors.doctor1;
        default:
            return realVideos.services.veneers;
    }
}

// Function to create video modal with real video
function createRealVideoModal(context, index = 0) {
    const videoData = getVideoByContext(context, index);
    const modal = document.createElement('div');
    modal.className = 'video-modal';
    modal.innerHTML = `
        <div class="modal-overlay">
            <div class="modal-content">
                <button class="modal-close">&times;</button>
                <div class="video-container">
                    <iframe 
                        src="https://www.youtube.com/embed/${videoData.videoId}?autoplay=1&rel=0&modestbranding=1" 
                        frameborder="0" 
                        allowfullscreen
                        title="${videoData.title}">
                    </iframe>
                </div>
            </div>
        </div>
    `;
    
    document.body.appendChild(modal);
    
    // Close modal functionality
    const closeBtn = modal.querySelector('.modal-close');
    const overlay = modal.querySelector('.modal-overlay');
    
    closeBtn.addEventListener('click', () => {
        document.body.removeChild(modal);
    });
    
    overlay.addEventListener('click', (e) => {
        if (e.target === overlay) {
            document.body.removeChild(modal);
        }
    });
    
    // Close on escape key
    document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape' && document.querySelector('.video-modal')) {
            document.body.removeChild(modal);
        }
    });
}

